// Statements in Javascript
// there are 2 main type of Statements: selection and repetition
// selection statements: execute some parts of the codeblock based on a certain condition;
// the following are basics of repetition and selection statements.

// if statement-selection statement.
// the code below checks if the value is greater than 32, if so it increments the value by 10.
let favouriteNUmber =02;
if(favouriteNUmber>=10){
    favouriteNUmber +=10;
}
console.log(favouriteNUmber);

// repetition statement- disrupt normal sequemce to check a condition 
let bestNumber =2;
while(bestNumber<=10){
    bestNumber += 5;
}
